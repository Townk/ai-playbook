// noBackendRegenNote builds the drift-region note for a regenerate that failed
// because no AI backend is available (F24). It names the CONFIGURED harness's
// binary — the same author.HarnessBin resolution the real invocation uses (cfg
// [agent].bin else the harness name) — instead of assuming any one harness.
func noBackendRegenNote() string {
	cfg, _ := config.Load() // always non-nil (Default on error)
	return fmt.Sprintf("no AI backend found — install and authenticate %s, or resolve manually", author.HarnessBin(cfg))
}

// looksLikeNoBackend reports whether err indicates the AI harness/backend is missing
// or unusable (F24) — the binary isn't on PATH, the harness isn't supported/built, or
// the model couldn't be resolved. Used to pick the specific "no AI backend available"
// drift note over the generic regenerate-failed message. Best-effort substring match:
// a false negative merely shows the generic (still non-silent) note.
func looksLikeNoBackend(err error) bool {
	if err == nil {
		return false
	}
	s := strings.ToLower(err.Error())
	for _, needle := range []string{
		"executable file not found",
		"no such file or directory",
		"not yet supported",
		"command not found",
		"regenerate unavailable",
		"no backend",
	} {
		if strings.Contains(s, needle) {
			return true
		}
	}
	return false
}

// driftMsg carries the result of an async drift check for a diff block. The
// handler sets blockRunState.Drifted when the verdict is DriftDrifted (the
// patch no longer applies cleanly) and clears it on DriftClean / DriftApplied.
type driftMsg struct {
	ID      string
	Verdict orchestrator.DriftVerdict
}

// driftRegenMsg carries the result of an async DriftRegen call: the fresh
// patch (NewPatch) regenerated by the model, or an Err on failure. Consumed by
// Task 5's handler, which splices the new patch into the block.
type driftRegenMsg struct {
	ID       string
	NewPatch string
	Err      error
}

type resultMsg struct {
	ID      string
	Exit    int
	Logpath string
	// TimedOut marks a run result the driver killed at its timeout ceiling;
	// TimedOutAfter is the EFFECTIVE duration that ceiling was (the block's
	// declared timeout= or the orchestrator default), so the failure line can
	// say "timed out after <d>" instead of reading as a plain failure.
	TimedOut      bool
	TimedOutAfter time.Duration
}

type blockRunState struct {
	Status    string // "running" | "ok" | "failed" | "stopped"
	Action    string // "apply" | "undo" | "run" — which action the in-flight result belongs to
	Exit      int
	Logpath   string
	Expanded  bool
	SpinFrame int
	Stopped   bool // user clicked stop on this block; suppress auto-followup when its result arrives

	// runStartedAt is stamped whenever the block is marked "running" (run,
	// apply/undo, rollback target) so the result handler can journal a
	// wall-clock duration (time.Since). Zero when the block never ran.
	runStartedAt time.Time

	// PreviousRun marks a block pre-seeded "ok" from a PRIOR run's journal
	// (`run --retry`): it renders "✓ done — previous run", keeps a LIVE run
	// button (its outputs don't exist in this session, so it stays manually
	// re-runnable), and satisfies needs= like any ok block. Cleared the moment
	// the block runs again in this session (every mark-running site), so a new
	// result renders and journals as an ordinary run.
	PreviousRun bool

	// TimedOut / TimedOutAfter surface a run killed at its timeout ceiling: the
	// failed summary line renders "✗ timed out after <TimedOutAfter>" instead of
	// the plain "✗ failed". Overwritten by every new result for the block, so a
	// re-run that fails normally never keeps a stale timed-out form.
	TimedOut      bool
	TimedOutAfter time.Duration

	// FollowupExhausted is set on the "verify" block once the auto-follow-up cap is
	// reached: the verify block normally hides the manual "try another fix" button
	// (it auto-fires), but past the cap auto-firing stops and the button is shown so
	// the user can keep iterating by hand. See render.go's failed-block button gate.
	FollowupExhausted bool

	// Drifted is set when an async drift check (driftCheckCmds) returns DriftDrifted:
	// the patch can no longer be applied or reversed — the target changed incompatibly.
	// Cleared when DriftClean (forward applies) or DriftApplied (already applied).
	// Tasks 3-4 read this to surface a warning badge on the diff block's render.
	Drifted bool

	// RegenFailed is set by Task 5's driftRegenMsg handler when DriftRegen returns an
	// error. Task 5's render note uses it to show a "regen failed" badge beside the
	// [regenerate] button so the user knows the attempt did not produce a fresh patch.
	RegenFailed bool

	// RegenNote, when non-empty, is the exact drift-region message shown after a FAILED
	// regenerate (F24) — it overrides the default "regenerate didn't resolve it" line so
	// a specific cause (e.g. "no AI backend available…") is surfaced instead of a silent
	// no-op. Set together with RegenFailed; cleared on a fresh regenerate attempt.
	RegenNote string

	// Resolved marks a diff block the user reconciled by hand to a CUSTOM state (via
	// resolve-manually) that the whole-patch git-apply can neither cleanly apply nor
	// fully reverse — e.g. a multi-hunk patch resolved partly to "expected" and partly
	// to "proposed". Such a block is terminal: greyed number, no drift region, no
	// Apply/Undo (there is nothing for git to apply or reverse). Distinct from Drifted
	// (unresolved) and Status "ok" (cleanly applied/undoable).
	Resolved bool

	// pendingResolve is a transient flag set by driftResolveFinish when a manual resolve
	// CHANGED the file; it tells the next driftMsg to treat a DriftDrifted verdict as a
	// custom manual resolution (→ Resolved) rather than an unresolved drift. Cleared by
	// that driftMsg. (An unchanged "kept current" resolve leaves it false, so the block
	// correctly stays Drifted.)
	pendingResolve bool

	// RollingBack / RolledBack decorate the FAILED block that triggered a rollback chain
	// (manual "Rollback playbook" or --auto-rollback): while the chain runs, RollingBack
	// shows a "rolling back applied steps…" spinner under the failure; when it finishes,
	// RolledBack appends a " — all steps rolled back" suffix to the failure line. (The
	// rolled-back TARGET blocks themselves render via Status "rolledback".)
	RollingBack bool
	RolledBack  bool
}

func (m model) handleResult(msg resultMsg) (tea.Model, tea.Cmd) {
	st := m.blockStates[msg.ID]
	prevAction := st.Action
	if dbgFile != nil {
		ids := make([]string, 0, len(m.blockStates))
		for id := range m.blockStates {
			ids = append(ids, id)
		}
		dbg("result id=%s exit=%d priorStatus=%q priorAction=%q knownBlockStates=%v", msg.ID, msg.Exit, st.Status, prevAction, ids)
	}
	st.Logpath = msg.Logpath
	st.Exit = msg.Exit
	st.TimedOut = msg.TimedOut
	st.TimedOutAfter = msg.TimedOutAfter
	// A settled result is THIS session's: the retry pre-seed marker never
	// survives a real run (defensive — every mark-running site clears it too).
	st.PreviousRun = false
	// A result for a block the user deliberately stopped is NOT a failed fix.
	// Resolve to the neutral "stopped" state, clear the flag, and never auto-fire
	// the follow-up — regardless of the (typically 143/SIGTERM) exit code.
	if st.Stopped {
		st.Status = "stopped"
		st.Action = ""
		st.Stopped = false
		m.blockStates[msg.ID] = st
		m.journalRecordResult(msg.ID, st, msg)
		dbg("result id=%s exit=%d STOPPED — no auto-followup", msg.ID, msg.Exit)
		// Stopping the in-flight chain step ends the materialization chain: clear
		// it here (this early return skips the chain-advance block below), or the
		// queued members would stay inert forever.
		if m.chainStep != "" && msg.ID == m.chainStep {
			m.chainQueue, m.chainStep = nil, ""
		}
		m.reflow()
		return m, nil
	}
	switch {
	case st.Action == "rollback":
		// A rollback-chain target (the undo command) finished. Exit 0 → a normal
		// success (✓ ran) — the rolled-back step it undid is what shows "↺ step
		// rolled back". A non-zero rollback is itself a failure (undo didn't run
		// cleanly).
		if msg.Exit == 0 {
			st.Status = "ok"
		} else {
			st.Status = "failed"
		}
		st.Action = ""
	case msg.Exit == 0 && st.Action == "undo":
		// Successful undo: patch is no longer applied; clear status so dependents re-lock.
		st.Status = ""
		st.Action = ""
		// Drop stale run results on blocks that (transitively) needed this one, so they
		// re-lock cleanly instead of showing a leftover "✓ ran" beside a blocked block.
		// Their journal records go with them — a re-locked block is unrun again.
		m.journalRemoveAll(resetDependents(m.blockStates, m.blocks, msg.ID))
	case msg.Exit != 0 && st.Action == "undo":
		// Failed undo: patch is still applied (graceful — surface error, keep button as undo).
		st.Status = "ok"
		// keep st.Action="" so the error region shows normally
		st.Action = ""
	case msg.Exit == 0:
		// Successful apply or run.
		st.Status = "ok"
		st.Action = ""
	default:
		// Failed apply or run.
		st.Status = "failed"
		st.Action = ""
	}
	m.blockStates[msg.ID] = st
	// Journal the settled result (ok/failed → record with exit+duration; a
	// successful undo → the record is removed). The deliberate-stop path
	// already recorded and returned above. A FAILED undo is the one result
	// that must NOT re-record: the block is still applied and its existing
	// record still describes the original apply (re-recording would journal
	// "ok" with the undo's non-zero exit, clobbering the real exit/duration).
	if prevAction != "undo" || msg.Exit == 0 {
		m.journalRecordResult(msg.ID, st, msg)
	}
	dbg("result id=%s exit=%d action=%s status->%s", msg.ID, msg.Exit, prevAction, st.Status)
	// From-chain advance: while a materialization chain is in flight (a consumer's
	// unrun producers running before it), only the EXPECTED in-flight step's own
	// run result moves the chain — msg.ID must match chainStep. runMu serializes
	// runs but an UNRELATED block's result can still land mid-window; it must
	// neither advance nor clear the chain. A rollback/apply/undo result for the
	// same id (prevAction) never advances a from-chain either. chainStep ok →
	// dispatch the next queued step (which becomes the new chainStep); the final
	// step's ok clears the chain; a failed chain step stops it (downstream steps
	// never start).
	if m.chainStep != "" && msg.ID == m.chainStep &&
		prevAction != "rollback" && prevAction != "apply" && prevAction != "undo" {
		if st.Status == "ok" {
			if len(m.chainQueue) > 0 {
				next := m.chainQueue[0]
				m.chainQueue = m.chainQueue[1:]
				m.chainStep = next
				m = m.markRunning(next)
				ac := m.emitAction(Button{Kind: "run", Payload: m.blockCommand(next), BlockID: next})
				m.reflow()
				return m, tea.Batch(m.startTick(), ac)
			}
			m.chainStep = "" // the final chain step landed — chain complete
		} else {
			m.chainQueue, m.chainStep = nil, "" // a failed chain step stops the chain
		}
	}
	// Assisted (GUIDED) advance hook: the readyID cursor only reacts to its OWN
	// step's result (never a rollback target's — prevAction=="rollback" is that
	// chain, handled below by the ordinary rollback bookkeeping instead).
	if m.assisted && msg.ID == m.readyID && prevAction != "rollback" {
		switch st.Status {
		case "ok":
			m = m.assistedAdvance()
		case "failed":
			m.assistedFailedID = msg.ID
			m.assistedFooter = "failure"
			m.footerFocus = 0
			m.exitCode = 1
		}
	}
	// A rollback target completed: count it down; when the whole chain is done, clear
	// the failed block's spinner and append its "all steps rolled back" suffix.
	if prevAction == "rollback" {
		if m.rollbackPending > 0 {
			m.rollbackPending--
		}
		m = m.finishRollbackIfDone()
	}
	m.reflow()
	// --auto-rollback: on a step failure, if the user opted in and there are applied
	// steps to undo, fire the rollback chain automatically (unattended runs) instead
	// of leaving the manual "Rollback playbook" button. Takes precedence over the
	// verify auto-followup below (rollback, not re-engage, is the opted-in response).
	// Guard against re-firing on a rollback TARGET's own result (prevAction rollback).
	if prevAction != "rollback" && st.Status == "failed" && m.autoRollback && m.anyRollbackable() && !m.assisted {
		return m.beginRollback(msg.ID)
	}
	// Auto-fire a follow-up when the VERIFY re-run fails: a non-zero exit on a
	// RUN result (not an apply/undo) for block id "verify" is the unambiguous
	// "the fix didn't work" signal. It fires on EACH verify failure — including
	// the re-armed follow-up playbook's own verify block, which reuses id=verify
	// and so flows through this same path — until the attempt cap (m.maxFollowups,
	// default 3, $AI_PLAYBOOK_MAX_FOLLOWUPS) is reached. Past the cap it stops
	// auto-firing and the manual "try another fix" button is shown on the verify
	// block instead (render.go gates that button on m.followups >= m.maxFollowups).
	//
	// NOTE: the previous once-only guard (prevStatus == "failed") meant the SECOND
	// verify failure — the re-armed playbook's verify, which leaves the block in
	// "failed" — was suppressed as "already fired", so the loop never auto-repeated.
	// The attempt counter replaces that guard.
	//
	// verifyID is the agent's {id=verify} tag; if the agent drifted and left its
	// blocks untagged (the parser then auto-names them), fall back to the LAST
	// runnable block as the verify so success/follow-up detection still works.
	verifyID := m.verifyBlockID()
	if msg.ID == verifyID && msg.Exit != 0 &&
		prevAction != "apply" && prevAction != "undo" && !m.assisted {
		switch {
		case m.followups >= m.maxFollowups:
			// Cap reached: stop auto-firing. Mark the verify block so render.go shows
			// the manual "try another fix" button, letting the user keep going by hand.
			dbg("auto-followup SUPPRESSED: cap reached (followups=%d max=%d id=%s)", m.followups, m.maxFollowups, msg.ID)
			vst := m.blockStates[msg.ID]
			vst.FollowupExhausted = true
			m.blockStates[msg.ID] = vst
			m.reflow()
		case msg.Exit > 128:
			// Signal-killed (e.g. 143=SIGTERM, 130=SIGINT): a deliberate kill is
			// not a fix failure — do NOT auto-fire. Ordinary non-zero exits
			// (1/2/…) still escalate to a follow-up below.
			dbg("auto-followup SUPPRESSED: signal-killed exit>128 (id=%s exit=%d)", msg.ID, msg.Exit)
		case msg.Exit == 127:
			// "command not found": the verify command itself couldn't run (e.g. the
			// original command is a shell alias/function absent from the agent's
			// non-interactive shell), NOT that the fix failed — do NOT auto-fire.
			// The manual "try another fix" button still appears (unchanged).
			dbg("auto-followup SUPPRESSED: exit 127 (command not found) id=%s", msg.ID)
		case !m.canReengageInProc():
			// No way to deliver the follow-up: in-process re-engagement is not wired
			// (no orch + Reengage). The live session path (file/stdin input, Reengage
			// set) does have it and so still auto-fires below.
			dbg("auto-followup SUPPRESSED: no in-process reengage (id=%s exit=%d)", msg.ID, msg.Exit)
		default:
			m.followups++
			dbg("auto-followup fire: id=%s exit=%d attempt=%d/%d", msg.ID, msg.Exit, m.followups, m.maxFollowups)
			// Issue #1+#2: announce this AUTO follow-up in the agent's voice ABOVE the
			// new attempt and scroll once so it becomes the top visible row. Only the
			// AUTO path narrates; the manual "try another fix" button does not.
			m.announceFollowup(m.followups)
			if cmd := m.beginFollowupStream(verifyID, m.blockCommand(verifyID)); cmd != nil {
				return m, cmd
			}
		}
	}
	// Stage 2 (spec §A): a SUCCESSFUL verify (exit 0 on a RUN, not an apply/undo)
	// means the fix verified — render the NATIVE in-pager confirm row INSTEAD of the
	// old agent-ask wrap-up. The ui owns the branch: Yes generates the final-playbook
	// draft (REPLACE); No dismisses the confirm (the command already succeeded, so
	// there is nothing to re-fix — the user can quit or press `c` to bring the
	// confirm back). Gated on m.wrappedUp so it shows
	// ONCE per resolution — a re-rendered or re-run verify-0 must not re-prompt. A
	// deliberately stopped verify already returned above; exit 0 is by definition
	// neither signal-killed (>128) nor 127. Requires in-process re-engagement (the
	// live session path), so the confirm's Yes can actually generate.
	if msg.ID == verifyID && msg.Exit == 0 && !m.wrappedUp &&
		prevAction != "apply" && prevAction != "undo" &&
		m.canReengageInProc() && !m.assisted {
		m.wrappedUp = true
		m.confirmResolved = true
		m.confirmFocus = 0 // default keyboard focus = Yes
		dbg("verify exit 0 — rendering native resolve-confirm row")
		m.reflow()
		// The confirm row appearing is a one-shot repaint; re-assert the hide so
		// zellij can't re-show the cursor on it.
		return m, reassertHideCursor()
	}
	return m, nil
}

func (m model) handleDrift(msg driftMsg) (tea.Model, tea.Cmd) {
	// Async drift-check result. The verdict fully describes the diff block's state
	// against its target file, so drive both Drifted AND the applied/not-applied
	// status off it:
	//   - DriftDrifted → the patch neither applies nor reverses: show the drift region.
	//   - DriftApplied → the file already matches the patch's NEW side (it was applied,
	//     or a manual resolve kept the "proposed" lines) → mark it APPLIED so the block
	//     shows Undo + a greyed number, not an Apply button.
	//   - DriftClean → the patch applies but isn't applied yet → Apply available.
	st := m.blockStates[msg.ID]
	switch msg.Verdict {
	case orchestrator.DriftDrifted:
		if st.pendingResolve {
			// A manual resolve CHANGED the file to a custom/mixed state the whole-patch
			// git-apply can neither apply nor reverse → terminal "resolved manually",
			// not unresolved drift. (An unchanged "kept current" resolve never sets
			// pendingResolve, so it correctly falls through to Drifted below.)
			st.Drifted = false
			st.Resolved = true
		} else {
			st.Drifted = true
		}
	case orchestrator.DriftApplied:
		st.Drifted = false
		st.Resolved = false
		st.Status = "ok"
	default: // DriftClean
		st.Drifted = false
		st.Resolved = false
		if st.Status == "ok" {
			st.Status = "" // not applied → don't linger as applied (Apply, not Undo)
		}
	}
	st.pendingResolve = false
	m.blockStates[msg.ID] = st
	m.reflow()
	return m, nil
}

func (m model) handleDriftRegen(msg driftRegenMsg) (tea.Model, tea.Cmd) {
	// Async DriftRegen result: clear the "regenerating" spinner status, then either
	// splice the fresh patch into m.md and re-check drift (success path), or keep the
	// block Drifted and set RegenFailed so the drift region shows the alternate note
	// (failure path). Never touches m.reader/structured/bodyProvider/streaming/thinking.
	st := m.blockStates[msg.ID]
	st.Status = ""
	if msg.Err != nil || strings.TrimSpace(msg.NewPatch) == "" {
		st.RegenFailed = true // block stays Drifted; render shows alternate message
		// F24: surface WHY it failed. A missing/unconfigured AI backend gets a
		// specific, actionable note; any other failure (or an empty patch) keeps the
		// generic "resolve manually" alternate — either way the drift region shows a
		// visible message, never a silent no-op.
		if looksLikeNoBackend(msg.Err) {
			st.RegenNote = noBackendRegenNote()
		} else {
			st.RegenNote = ""
		}
		m.blockStates[msg.ID] = st
		m.reflow()
		return m, nil
	}
	st.RegenFailed = false
	st.RegenNote = ""
	m.blockStates[msg.ID] = st
	if newMd, ok := replaceBlockBody(m.md, msg.ID, msg.NewPatch); ok {
		m.md = newMd
	}
	m.reflow()
	return m, m.driftCheckCmds() // re-check all diff blocks; clears Drifted if fresh patch applies
}
